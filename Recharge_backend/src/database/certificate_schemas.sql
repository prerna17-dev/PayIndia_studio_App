    `date_of_marriage` DATE NOT NULL,
    `place_of_marriage` VARCHAR(255) NOT NULL,
    `marriage_address` TEXT,
    `type_of_marriage` ENUM('Religious', 'Court', 'Other') NOT NULL,
    `w1_name` VARCHAR(255),
    `w1_aadhaar` VARCHAR(12),
    `w1_address` TEXT,
    `w1_mobile` VARCHAR(10),
    `w2_name` VARCHAR(255),
    `w2_aadhaar` VARCHAR(12),
    `w2_address` TEXT,
    `w2_mobile` VARCHAR(10),
    `groom_aadhaar_url` VARCHAR(255),
    `bride_aadhaar_url` VARCHAR(255),
    `invitation_card_url` VARCHAR(255),
    `venue_proof_url` VARCHAR(255),
    `marriage_photos_url` VARCHAR(255),
    `w1_aadhaar_url` VARCHAR(255),
    `w2_aadhaar_url` VARCHAR(255),
    `w1_photo_url` VARCHAR(255),
    `w2_photo_url` VARCHAR(255),
    `address_proof_url` VARCHAR(255),
    `status` ENUM('Pending', 'Processing', 'Completed', 'Rejected') DEFAULT 'Pending',
    `reference_id` VARCHAR(20) UNIQUE,
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 7. EWS Certificates
