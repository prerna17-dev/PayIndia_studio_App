-- Database schema for 8 Certificate Services

-- 1. Income Certificates
CREATE TABLE IF NOT EXISTS `income_certificates` (
    `id` INT AUTO_INCREMENT PRIMARY KEY,
    `user_id` INT NOT NULL,
    `full_name` VARCHAR(255) NOT NULL,
    `aadhaar_number` VARCHAR(12) NOT NULL,
    `mobile_number` VARCHAR(10) NOT NULL,
    `email` VARCHAR(255),
    `pan_number` VARCHAR(10),
    `dob` DATE NOT NULL,
    `gender` ENUM('Male', 'Female', 'Other') NOT NULL,
    `occupation` VARCHAR(255),
    `annual_income` DECIMAL(15, 2) NOT NULL,
    `income_source` VARCHAR(255),
    `purpose` VARCHAR(255),
    `house_no` VARCHAR(255),
    `street` VARCHAR(255),
    `village` VARCHAR(255),
    `taluka` VARCHAR(255),
    `district` VARCHAR(255),
    `state` VARCHAR(255),
    `pincode` VARCHAR(6),
    `aadhaar_card_url` VARCHAR(255),
    `ration_card_url` VARCHAR(255),
    `tax_receipt_url` VARCHAR(255),
    `income_proof_url` VARCHAR(255),
    `self_declaration_url` VARCHAR(255),
    `photo_url` VARCHAR(255),
    `other_docs_url` VARCHAR(255),
    `status` ENUM('Pending', 'Processing', 'Completed', 'Rejected') DEFAULT 'Pending',
    `reference_id` VARCHAR(20) UNIQUE,
    `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

